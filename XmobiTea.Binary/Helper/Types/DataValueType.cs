namespace XmobiTea.Binary.Helper.Types
{
    /// <summary>
    /// Represents the various data types that can be used in binary operations.
    /// </summary>
    enum DataValueType
    {
        /// <summary>Represents a Boolean value.</summary>
        Bool,

        /// <summary>Represents a byte value.</summary>
        Byte,

        /// <summary>Represents a character value.</summary>
        Char,

        /// <summary>Represents a double-precision floating point value.</summary>
        Double,

        /// <summary>Represents a single-precision floating point value.</summary>
        Float,

        /// <summary>Represents a 32-bit signed integer value.</summary>
        Int,

        /// <summary>Represents a 64-bit signed integer value.</summary>
        Long,

        /// <summary>Represents an 8-bit signed integer value.</summary>
        SByte,

        /// <summary>Represents a 16-bit signed integer value.</summary>
        Short,

        /// <summary>Represents a string value.</summary>
        String,

        /// <summary>Represents a 32-bit unsigned integer value.</summary>
        UInt,

        /// <summary>Represents a 64-bit unsigned integer value.</summary>
        ULong,

        /// <summary>Represents a 16-bit unsigned integer value.</summary>
        UShort,

    }
}
